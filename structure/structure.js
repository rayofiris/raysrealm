function showPlay1() 
{
    let play1= document.querySelector(".play1");
    let note1= document.querySelector(".note1"); 
    let bnatural= document.getElementById("bnatural"); 
    play1.style.visibility="visible";
    bnatural.play();
    note1.addEventListener("click", event => {
if (play1.style.visibility ==="hidden") {
 play1.style.visibility = "visible";
bnatural.play();}
else{ play1.style.visibility ="hidden";
    bnatural.stop();
}
});
}

